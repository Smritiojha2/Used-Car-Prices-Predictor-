function stateDropdown(data, stateFilter) {

    const filteredArray = data.filter(m => m[0] === stateFilter);

    const uniqueList = getUniqueValues(filteredArray, 1);

    const selectCity = document.getElementById("city");

    populateDropDown(selectCity, uniqueList);

}

function afterDocumentLoads() {
    populateStateLevelDropDown();
    applyDropDown();
}

function applyDropDown() {
    const selectStateValue = document.getElementById("state").value;
    stateDropdown(stateData, selectStateValue);
}

function getUniqueValues(data, index){
    const uniqueOptions = new Set();
    data.forEach(m => uniqueOptions.add(m[index]));
    return [...uniqueOptions];
}

function populateStateLevelDropDown(){
    const uniqueList = getUniqueValues(stateData, 0);
    const el = document.getElementById("state");
    populateDropDown(el, uniqueList);
}


function populateDropDown(el, listAsArray){
    el.innerHTML = "";

    listAsArray.forEach(item => {
        const option = document.createElement('option');
        option.textContent = item;
        el.appendChild(option);
    });
}

document.getElementById("state").addEventListener("change",applyDropDown);
document.addEventListener("DOMContentLoaded", afterDocumentLoads);


